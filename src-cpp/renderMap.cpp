void Map::DrawMap(SDL_Renderer* renderer) {
    SDL_Rect tileRect; // Rectangle to represent each tile

    // Set the size of each tile
    tileRect.w = TILE_SIZE;
    tileRect.h = TILE_SIZE;

    for (int i = 0; i < MAP_HEIGHT; i++) {
        for (int j = 0; j < MAP_WIDTH; j++) {
            // Calculate the position of the tile
            tileRect.x = j * TILE_SIZE;
            tileRect.y = i * TILE_SIZE;

            // Set color based on the tile value
            if (map[i][j] == 1) {
                SDL_SetRenderDrawColor(renderer, 255, 0, 0, 255); // Red for 1
            } else if (map[i][j] == 0) {
                SDL_SetRenderDrawColor(renderer, 0, 0, 255, 255); // Green for 0
            }

            // Render the rectangle
            SDL_RenderFillRect(renderer, &tileRect);
        }
    }
}

